                <br>
                <p>Ophthalmology (/ˌɒfθælˈmɒlədʒi/ or /ˌɒpθælˈmɒlədʒi/)[1] is a branch of medicine and surgery (both methods are used) that deals with the anatomy, physiology and diseases of the eyeball and orbit.[2] An ophthalmologist is a specialist in medical and surgical eye disease. Their credentials include a doctorate degree in medicine, followed by an additional four years of Ophthalmology residency training. They may or may not receive residency training in internal medicine, pediatrics, or general surgery before the ophthalmology residency. Additional training may be sought through a fellowship in a particular specialty of eye pathology.[3]  Ophthalmologists are allowed to medically treat eye disease, implement laser therapy, and perform surgery when needed.[4]  Ophthalmologists may participate in academic research on the diagnosis and treatment for eye disorders.[5] </p>
                
                
                  
                
                
              <h2>Diseases:</h2>
                
                <p>A partial list of the most common diseases diagnosed and treated by Ophthalmologists include:[6]
Cataract
Glaucoma
Macular degeneration
Diabetic retinopathy
Dry eyes
Strabismus (misalignment/deviation of eyes)
Proptosis (bulged eyes)
Excessive tearing (nasolacrimal duct obstruction)
Uveitis
Eye tumors
Refractive surgery</p>
                
                
                <h2>Diagnosis</h2>
                
                <h2>Eye examination</h2>
                
                <p> 
Following are examples of methods of diagnosis performed in a eye examination
Visual acuity checked by optometrist
Refraction done by optometrist
Intraocular pressure measured by optometrist and ophthalmologist
Slit lamp examination done by both optometrist and ophthalmologist
Retina examination
</p>
                
                
                <h2>Specialized tests</h2>
                
                <p>Optical coherence tomography performed by Optometrist and ophthalmic technician
                Fluorescein angiography performed by Optometrist and Ophthalmic technician
                Ultrasonography.</p>
                
                <h>Ophthalmic surgery</h>
                
                <p>Eye surgery, also known as ocular surgery, is surgery performed on the eye or its adnexa by an ophthalmologist.The eye is a fragile organ, and requires extreme care before, during, and after a surgical procedure. An expert eye surgeon is responsible for selecting the appropriate surgical procedure for the patient, and for taking the necessary safety precautions. </p>
                
                <h2>Subspecialties</h2>
                
                <p>Ophthalmology includes subspecialities which deal either with certain diseases or diseases of certain parts of the eye. Some of them are:[7]
Anterior segment surgery
Cornea, ocular surface, and external disease
Glaucoma
Medical retina, deals with treatment of retinal problems through non-surgical means.
Vitreo-retinal surgery, deals with surgical management of retinal and posterior segment diseases and disorders. Medical retina and vitreo-retinal surgery sometimes together called posterior segment subspecialisation, whereas management and research of the vitreousexclusively is sometimes referred to as vitreology
Neuro-ophthalmology
Ocular oncology
Oculoplastics and orbit surgery
Ophthalmic pathology
Pediatric ophthalmology/strabismus (misalignment of the eyes)
Refractive surgery
Uveitis
Veterinary specialty training programs in veterinary ophthalmology exist in some countries.</p>
                
                <h2>Signs and Symptoms of Vision Problems</h2>
                
                <p>If you experience any of the following eye changes, schedule an appointment with your ophthalmologist or optometrist immediately, even if you've been to your eye doctor recently:
Severe, sudden eye pain
Recurrent pain in or around the eye
Hazy, blurred, or double vision
Seeing flashes of light or sudden bright floating spots
Seeing rainbows or halos around lights
Seeing floating "spider webs"
Seeing a "curtain coming down" over one eye
Sensing a "cup filling up with ink" in one eye
Unusual, even painful, sensitivity to light or glare
Swollen, red eyes
Changes in the color of the iris
White areas in the pupil of the eye
Sudden development of persistent floaters
Itching, burning, or a heavy discharge in the eyes
Any sudden change in vision
See also Normal Vision Changes to help you understand normal age-related changes in the eyes and your vision.
</p>                

