                    <h1> *Brain Tumor Surgery :</h1>
                    <p>The surgical procedure is performed for brain tumors that can be removed without resulting in severe damage. A large number of non-cancerous (benign) tumors can only be removed by surgery. However, cancerous (malignant) tumor may require additional treatment apart from surgery like chemotherapy or radiation therapy. The aim of the surgery is to remove the tumor as much as possible and also reducing its symptoms so as to improve the quality of life.</p>
                    <h1>*Brain Aneurysm Surgery : </h1>
                    <p>It is not necessary that every aneurysm requires treatment. Basically there are two surgical techniques that treat the condition of brain aneurysms- endovascular coiling and neurosurgical clipping.</p>
                    <h1>*Endoscopic Brain Surgery : </h1>
                    <p>
                        This surgery is basically meant for treating skull base tumors. An endoscopic-assisted surgery results in no scars.
                        Minimally Invasive Brain Surgery : Earlier the problem of brain tumors was treated with the help of craniotomies and also by more invasive skin incisions. These procedures often resulted in more post-operative pain and also involves more recovery time. Many technological advancements in image guidance and endoscopic visualization have introduced less invasive surgery for both spine and brain. This surgery is accomplished through endoscopic approaches (through a small incision), endonasal approaches (through the nose) and supraorbital approaches (above the eye).
                    </p>
                    <h1>*Bleeding Brain Surgery :</h1>
                    <p>Bleeding in the brain or in the space between the skull and the brain requires surgery. The surgical procedure is performed for relieving the pressure in the skull and for removing the blood that has built up in the brain. Surgery is the only option by which blood clots are removed surrounding the surface of the brain.</p>
                    <h1>*Meningioma Brain Surgery : </h1>
                    <p>meningioma tumor grows on the surface of the brain. This tumor rather than growing inside the brain, pushes the brain away. Most of these types of tumors are considered as benign. This rare tumor exhibit cancer-like behavior known as anaplastic meningiomas or meningiomas. They represent almost 20% of all the tumors that occurs in the head. The surgery for treating meningioma is a very complex procedure. The aim of the surgery is to preserve the neurological function, removing of sufficient tumor so as to relieve pressure on the normal brain tissue and also removal of the entire tumor so that it will never occur again.</p>
                    <h1>*Awake Brain Surgery :</h1>
                    <p>This type of surgery is performed when the patient is awake for treating neurological conditions such as epileptic seizures or brain tumors. A patient is made awake during the surgical operation when the tumor is near the parts of the brain controlling speech or movement. The responses of the patient ensure surgeon about the precise area of the brain requiring surgery. Also this procedure lowers down the risk of damage to the brain’s functional area. This surgery has many advantages. The patients who have brain tumors around the brain’s functional tissue and also their condition was inoperable earlier, can opt for awake brain surgery. This surgery reduces the tumor’s size and also improves overall quality of life</p>

