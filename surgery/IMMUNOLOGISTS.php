                <p>An allergist / immunologist (commonly referred to as an allergist) is a physician specially trained t diagnose, treat and manage allergies, asthma and immunologic disorders including primary immunodeficiency disorders. These conditions range from the very common to the very rare, spanning all ages and encompassing various organ systems</p>
                <p>The immune system is supposed to protect the body from illness, infection, and disease. Sometimes the immune system makes a mistake and attacks the healthy cells of your body instead of protecting them. When this happens, it is called an autoimmune disease (also called an autoimmune disorder). Although there are many different types of autoimmune disorder that affect different parts of the body, two types of autoimmune disorder include rheumatoid (ROO-muh-toyd) arthritis and lupus.</p>
            <p>No one knows why autoimmune disorders happen, although they tend to run in families. Women have a higher chance of developing an autoimmune disorder, especially African-American, Native American, and Hispanic women.The immune system is supposed to protect the body from illness, infection, and disease. Sometimes the immune system makes a mistake and attacks the healthy cells of your body instead of protecting them. When this happens, it is called an autoimmune disease (also called an autoimmune disorder). Although there are many different types of autoimmune disorder that affect different parts of the body, two types of autoimmune disorder include rheumatoid (ROO-muh-toyd) arthritis and lupus.</p>    
                
                
              <h2>Symptoms</h2>
                
                <p>Signs and symptoms of an autoimmune disease (Allergy) vary, both in severity and type. They usually include muscle aches, fatigue (being very tired), a low fever, and inflammation (in-fluh-MAY-shun).
Inflammation happens when the body’s immune system is attacking something in your body. Five signs of inflammation are: redness, heat, swelling, pain, and loss of function. Loss of function is when the part of your body that is inflamed can no longer move properly. Not all five signs occur with inflammation, and sometimes inflammation can happen without any symptoms.
Once you are diagnosed with an autoimmune disorder, you can treat the symptoms, but it doesn’t ever go away. There may be times it flares up and gets worse, and other times when it goes into remission (re-MISH-on) when the symptoms seem to get better or disappear.</p>
                
                <h2>When to See a Doctor</h2>
                
                <p>If you feel any of these signs or symptoms, it is important for you to contact your health care provider as soon as possible to find out what is happening. There are more than 80 types of autoimmune disease, and many have similar symptoms. Sometimes an autoimmune disease can be difficult to diagnose, and may take a lot of time and more than one visit or doctor. The sooner you start trying to diagnose it, the better.</p>
                
                <h2>Causes</h2>
                
                <p>There are no known causes for autoimmune disorders at this time, although they do tend to run in families. Women, especially Hispanic, African-American, and Native American women, tend to have a higher chance to develop an autoimmune condition. Also, you have more chances of developing more autoimmune conditions if you already have an autoimmune disorder.</p>
                
                <h2>Conditions Treated</h2>
                
                <p>The following conditions are some of the most common conditions treated by specialists in this area. These specialists offer expert care for many other related medical problems. If you need care for a condition not listed here, please call (888) 352-RUSH (7874) to find a doctor who can help you.</p>
                
                <p>Allergic Rhinitis (Allergies)
Allergic rhinitis is an allergic reaction to inhaled substances. There are two main types: seasonal allergic rhinitis and perennial allergic rhinitis. Symptoms may include sneezing, itchy eyes and hoarseness. 
Angioedema
Angioedema is swelling under the skin, rather than on the skin as is the case with hives. Angioedema swelling typically occurs around the eyes and lips. Hereditary angioedema is a rare, inherited immune disorder that causes swelling, particularly in the face and throat.
Asthma
Asthma is inflammation of the airways in your lungs.
Atopic Dermatitis (Eczema)
Atopic dermatitis, also known as eczema, is a very common skin condition. It often starts with an itch. Scratching results in a rash — dry, red patches that in some people may bubble and ooze.
Autoimmune Disorders
Autoimmune disorders are when a person’s immune system attacks healthy tissue and destroys it. Sometimes referred to as autoimmune diseases, these disorders include lupus, multiple sclerosis and rheumatoid arthritis.
Bronchitis
Bronchitis is an inflammation of the bronchial tubes carrying air to and from your lungs.
Celiac Disease
For people who have celiac disease, eating gluten causes the immune system to attack the lining of the small intestine. (Gluten is a protein found in wheat, rye and barley.) The resulting damage can make it hard for the body to get enough nutrients from food.  
Contact Dermatitis
Contact dermatitis is irritation from something that touches your skin, causing it to become red, sore, itchy or swollen.
Chronic Cough
Adult and pediatric specialists at Rush treat conditions causing chronic cough, which lasts for weeks, affects quality of life and has many causes.
Food Allergy
With food allergies, the body's immune system reacts to food as if it were a harmful substance, releasing chemicals that can cause hives, shortness of breath, tongue swelling and other symptoms.
Food Sensitivity
Food sensitivities usually result from the digestive system’s reaction to enzymes or proteins in food. They are sometimes mistaken for food allergies.
Hives (Urticaria)
Hives are itchy welts on skin that often arise from an allergic response to food or another substance.</p>
                
