        Abnormal heart rhythms, or arrhythmia
        <br> <br> <br>



        "Arrhythmia" means your heartbeat is irregular. It doesn't necessarily mean your heart is beating too fast or too slow. It just means it's out of its normal rhythm.<br>
        It may feel like your heart skipped a beat, added a beat, is "fluttering," or is beating too fast (which doctors call tachycardia) or too slow (called bradycardia). Or, you might not notice anything, since some arrhythmias are "silent."<br>
        Arrhythmias can be an emergency, or they may be harmless. If you feel something unusual happening with your heartbeat, call 911 so doctors can find out why it's happening and what you need to do about it.<br>
        <b>Causes and Types of of Arrhythmias</b>
        You could have an arrhythmia even if your heart is healthy. Or it could happen because you have:<br>
        <li>Heart disease</li>
        <li>The wrong balance of electrolytes (such as sodium or potassium) in your blood</li>
        <li>Changes in your heart muscle</li>
        <li>Injury from a heart attack</li>
        <li>Healing process after heart surgery</li>
        The many types of arrhythmias include:<br>
        <b>Premature atrial contractions.</b> These are early extra beats that start in the heart's upper chambers, called the atria. They are harmless and generally don't need treatment.
        <b> Premature ventricular contractions (PVCs).</b> These are among the most common arrhythmias.They're the "skipped heartbeat" we all occasionally feel. They can be related to stress or too much caffeine or nicotine. But sometimes, PVCs can be caused by heart disease or electrolyte imbalance. If you have a lot of PVCs, or symptoms linked to them, see a heart doctor (cardiologist).<br>
        <b>Atrial fibrillation.</b> This common irregular heart rhythm causes the upper chambers of the heart to contract abnormally.<br>

        <b>Atrial flutter.</b> This is an arrhythmia that's usually more organized and regular than atrial fibrillation. It happens most often in people with heart diseaseand in the first week after heart surgery. It often changes to atrial fibrillation.<br>
        <b>Paroxysmal supraventricular tachycardia (PSVT).</b> A rapid heart rate, usually with a regular rhythm, starting from above the heart's lower chambers, or ventricles. PSVT begins and ends suddenly.<br>
        <b>Accessory pathway tachycardias.</b> You can get a rapid heart rate because there is an extra pathway between the heart's upper and lower chambers. It's just like if there was an extra road on your way home as well as your usual route, so cars can move around faster. When that happens in your heart, it can cause a fast heart rhythm, which doctors call tachycardia. The impulses that control your heart rhythm travel around the heart very quickly, making it beat unusually fast.<br>
        <b>AV nodal reentrant tachycardia. </b>This is another type of fast heartbeat. It's caused by there being an extra pathway through a part of the heart called the AV node. It can cause heart palpitations, fainting, or heart failure. In some cases, you can stop it simply by breathing in and bearing down. Some drugs can also stop this heart rhythm.<br>
        <b>Ventricular tachycardia (V-tach). </b>A rapid heart rhythm starting from the heart's lower chambers. Because the heart is beating too fast, it can't fill up with enough blood. This can be a serious arrhythmia -- especially in people with heart disease -- and it may be linked to other symptoms.<br>
        <b>Ventricular fibrillation.</b> This happens when the heart's lower chambers quiver and can't contract or pump blood to the body. This is a medical emergency that must be treated with CPR and defibrillation as soon as possible.<br>
        <b>Long QT syndrome. </b>This may cause potentially dangerous arrhythmias and sudden death. Doctors can treat it with medications or devices called defibrillators.<br>
        <b>Bradyarrhythmias.</b> These are slow heart rhythms, which may be due to disease in the heart's electrical system. When this occurs, you may feel like you are going to pass out, or actually pass out. This could also be from medication. The treatment for this could be a pacemaker. <br>
        If you experience this, call your doctor.<br>
        <b>Sinus node dysfunction.</b> This slow heart rhythm is due to a problem with the heart's sinus node. Some people with this type of arrhythmia need a pacemaker.<br>
        <b>Heart block. </b>There is a delay or a complete block of the electrical impulse as it travels from the heart's sinus node to its lower chambers. The heart may beat irregularly and, often, more slowly. In serious cases, you'd get a pacemaker.<br>
        <h3>Symptoms of Arrhythmias</h3>
        An arrhythmia can be silent, meaning you don't notice any symptoms. A doctor can find an irregular heartbeat during a physical exam by taking your pulse or through an electrocardiogram (ECG or EKG).<br>
        If you do have symptoms, they may include:<br>
        <li>Palpitations (a feeling of skipped heart beats, fluttering or "flip-flops")
        <li>Pounding in your chest</li>
        <li>Dizziness or feeling light-headed</li>
        <li>Fainting</li>
        <li>Shortness of breath</li>
        <li>Chest pain or tightness</li>
        <li>Weakness or fatigue (feeling very tired)</li>
        <li>Diagnosis of Arrhythmias</li>
        <li>To diagnose an arrhythmia or find its cause, doctors use tests including:<br>
            <b>
        <li>Electrocardiogram --</li></b> Also called an EKG or ECG, this test records the electrical activity of your heart. You wear small electrode patches on your chest, arms, and legs for the quick, painless test, which you take in your doctor's office.<br>
        <b>
            <li>Holter monitor --</li>
        </b> This is a portable EKG that you'll use for 1 to 2 days. You'll have electrodes taped to your skin. It's painless and you can do everything but shower while wearing the electrodes.<br>
        <b>
            <li>Event monitor -- </li>
        </b>If your symptoms don't happen often, your doctor may suggest you wear one of these, usually for about a month. This is a device that, when you push a button, will record and store your heart's electrical activity for a few minutes. Each time you notice symptoms, you should try to get a reading on the monitor. Your doctor will interpret the results.<br>
        <b>
            <li>Stress test --</li>
        </b> There are different kinds of stress tests. The goal is to check how much stress your heart can manage before having a heart rhythm problem or not getting enough blood flow to the heart. For the most common type of stress test, you'll walk on a treadmill or pedal a stationary bike at increasing levels of difficulty while you're getting an EKG and getting your heart rate and blood pressure monitored.<br>
        <b>
            <li>Echocardiogram --</li>
        </b> This test uses ultrasound to evaluate heart muscle and heart valves.
        <b>
            <li>Cardiac catheterization --</li>
        </b> Your doctor will insert a long, thin tube, called a catheter, into a blood vessel in your arm or leg. She will guide it to your heart with help from a special X-ray machine. Then she'll inject dye through the catheter to help make X-ray videos of your heart's valves, coronary arteries, and chambers.<br>
        <b>
            <li>Electrophysiology study --</li>
        </b> This test records your heart's electrical activities and pathways. It can help find out what's causing heart rhythm problems and find the best treatment for you. During the test, your doctor will safely reproduce your abnormal heart rhythm and then may give you different medications to see which controls it best, or to see what procedure or device you need to treat it.<br>
        <b>
            <li>Head-up tilt table test --</li>
        </b> Doctors use this test to find out what's causing fainting spells. It measures the difference in heart rate and blood pressure when you're standing up or lying down. You'll get this test in a lab. You'll lie on a stretcher, tilted at different angles while you're getting an EKG and specialists are checking your blood pressure and oxygen level. This shows if symptoms of passing out are due to the electrical system, the nervous system, or the vascular system.<br>
        <h3> Treatment of Arrhythmias</h3>
       <b> What Is Electrical Cardioversion?</b>
       <p>If drugs can't control a persistent irregular heart rhythm (such as atrial fibrillation), you might need cardioversion. For this, doctors, give you a short-acting anesthesia, then deliver an electrical shock to your chest wall to allow the normal rhythm to restart.</p> 
        What Is a Pacemaker?
       <p>This device sends small electrical impulses to the heart muscle to keep a safe heart rate. The pacemaker has a pulse generator (which houses the battery and a tiny computer) and wires that send impulses from the pulse generator to the heart muscle.</p> 
        What Is an Implantable Cardioverter Defibrillator (ICD)?
        Doctors mainly use ICDs to treat ventricular tachycardia and ventricular fibrillation, two life-threatening heart rhythms.
       <p> The ICD constantly tracks the heart rhythm. When it detects a very fast, abnormal heart rhythm, it delivers an electric shock to the heart muscle to cause the heart to beat in a normal rhythm again. There are several ways the ICD can be used to restore normal heart rhythm. They include:</p>
       <b> Anti-tachycardia pacing (ATP).</b><p>When the heart beats too fast, you get a series of small electrical impulses to the heart muscle to restore a normal heart rate and rhythm.</p> 
       <b>Cardioversion.</b> <p> You may get a low-energy shock at the same time as the heart beats to restore normal heart rhythm.</p>
       <b> Defibrillation</b> <p>When the heart is beating dangerously fast or irregularly, the heart muscle gets a higher-energy shock to restore a normal rhythm.</p>
       <b>Anti-bradycardia pacing.</b>  Many ICDs provide back-up pacing to maintain heart rhythm if it slows too much.
       <b> What Is Catheter Ablation?</b>
        <p>You can think of this procedure as rewiring to fix an electrical problem within the heart.
        The doctor will insert a catheter through the leg. The catheter delivers high-frequency electrical energy to a small area inside the heart that causes the abnormal heart rhythm. This energy "disconnects" the pathway of the abnormal rhythm.
            Doctors use ablation to treat most PSVTs, atrial flutter, atrial fibrillation, and some atrial and ventricular tachycardias. Some people also need other procedures.</p>
      <b> Heart Surgery for Arrhythmias</b> 
       <p> The Maze procedure is a type of surgery used to correct atrial fibrillation. During this procedure, the surgeon makes a series, or "maze," of cuts in the heart's upper chambers. The goal is to keep the heart's electrical impulses only on certain pathways. Some people may need a pacemaker afterward.</p>
