        <h3>Treatments for Heart Attack and Unstable Angina</h3>
        <b>Heart Attack <br>
            Immediate treatment</b>
        Emergency assessment and treatment should be conducted quickly. Treatment will depend on when your symptoms started and how soon you can access treatment.
        If symptoms started within the last 12 hours, percutaneous coronary intervention (PCI) will be offered.1,2
        If symptoms started within the past 12 hours but you cannot access PCI quickly, medication to break down the blood clots will be prescribed.
        If symptoms started more than 12 hours ago, and symptoms have improved, the best course of treatment will be determined by an angiogram. The following may be proposed: medication, PCI or bypass surgery.1,2<br>
        <b>Anticoagulant Medications 3‒5</b>
        Anticoagulants are types of drugs that manipulate the blood coagulation process (the so-called plasmatic coagulation). They inhibit thrombus formation, meaning that they prevent blood clots from forming too easily. Anticoagulants generally include non-VKA oral anticoagulants (NOACs), heparins and vitamin K antagonists. All of these have different ways of working. Due to the pharmacological mode of action, the use of anticoagulants may be associated with an increased risk of occult or overt bleeding from any tissue or organ which may results in post haemorrhagic anaemia. The signs, symptoms and severity will vary according to the location (e.g. blood in urine, severe bruising) and degree or extent of the bleeding and/ or anaemia. Your doctor will work out which anticoagulant is right for you based on the indication and the presence of any other medical conditions. All licensed anticoagulants deliver a positive benefit risk profile. Although all anticoagulants can increase the risk of bleeding, strategies are available to reduce or stop bleeding.<br>
        <b>Antiplatelet medicines5,6</b>
        Antiplatelet medicines (e.g. Aspirin7, Clopidogrel8, Ticagrelor9, Prasugrel10) are types of drugs that decrease platelet aggregation. They inhibit thrombus formation and are mainly effective in the arterial circulation . Your doctor will work out which antiplatelet is right for you based on the indication and the presence of any other medical conditions. All licensed antiplatelets deliver a positive benefit risk profile. Although all antiplatelets can increase the risk of bleeding, strategies are available to reduce or stop bleeding.<br>
        <b>Fibrinolytic Drugs6,11</b>
        Fibrinolytic drugs activate plasminogen (the inactive precursor to plasmin), to form plasmin (an enzyme in the blood) which breaks down fibrin. This, in turn, breaks up clots. Drugs in this class include streptokinase.<br>
        <h3>Post-occurrence treatment</h3>
        <b>Post-occurrence1,2</b><br>
        The following drug treatments are usually prescribed to a person who has recently had a heart attack:<br>
        Angiotensin-converting enzyme (ACE) Inhibitors7,12
        ACE inhibitors are a class of medicines that cause blood vessels to relax, lowering blood pressure and increasing the supply of oxygen to the heart.
        Drugs in this class include ramipril and enalapril.<br>
        <b>Beta-blockers13</b><br>
        Are a class of drugs that may help to prevent attacks of angina and reduce your risk of having a further heart attack, by lowering high blood pressure, relieving chest pain, controlling irregular heart beat and protecting the heart in the early treatment post heart attack.<br>
        Drugs in this class include propranolol and atenolol.<br>
        <b>Statins9,14</b><br>
        Are a class of drugs that reduce blood cholesterol levels, by reducing the amount of cholesterol produced by the liver.
        Drugs in this class include Simvastatin.
        <b>Unstable Angina15</b>
        <br>
        As a result of having unstable angina you may be at a higher risk of having a heart attack, so tests can be conducted to look at the level of risk. If there is a high risk, an examination to assess the size and location of the blockage in the coronary artery can be carried out. If the blockage is large, surgery may be necessary in the form of a coronary artery bypass graft or a percutaneous coronary intervention.<br>
        <b>1.Glycerol Trinitrate (GTN)16</b><br>
        GTN, in contrast to clopidogrel and aspirin, is not used as a regular prophylactic medication but is instead used to treat episodes of high blood pressure and chest pain. GTN acts to widen blood vessel walls, which reduces blood pressure and alleviates chest pain. GTN is taken orally in the form of a sublingual spray or as a tablet dissolved under the tongue.<br>

