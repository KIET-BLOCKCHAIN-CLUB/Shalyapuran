        <h1>3.Coronary Artery Disease:</h1>

        <h3>Causes of coronary artery disease</h3>
        The most common cause of CAD is vascular injury with cholesterol plaque buildup in the arteries, known as atherosclerosis. Reduced blood flow occurs when one or more of these arteries becomes partially or completely blocked.
        The four primary coronary arteries are located on the surface of the heart:
        <li>right main coronary artery</li>
        <li>left main coronary artery</li>
        <li>left circumflex artery</li>
        <li>left anterior descending artery</li>
        These arteries bring oxygen and nutrient-rich blood to your heart. Your heart is a muscle that’s responsible for pumping blood throughout your body. According to the Cleveland Clinic, a healthy heart moves approximately 3,000 gallons of blood through your body every day.<br>
        Like any other organ or muscle, your heart must receive an adequate, dependable supply of blood in order to carry out its work. Reduced blood flow to your heart can cause symptoms of CAD.<br>
        Other rare causes of damage or blockage to a coronary artery also limit blood flow to the heart.<br>

        <h3>Symptoms of CAD</h3>
        When your heart doesn’t get enough arterial blood, you may experience a variety of symptoms. Angina (chest discomfort) is the most common symptom of CAD. Some people describe this discomfort as:
        <li>chest pain</li>
        <li>heaviness</li>
        <li>tightness</li>
        <li>burning</li>
        <li>squeezing</li>
        These symptoms can also be mistaken for heartburn or indigestion.
        Other symptoms of CAD include:<br>
        <li>pain in the arms or shoulders</li>
        <li>shortness of breath</li>
        <li>sweating</li>
        <li>dizziness</li>
        You may experience more symptoms when your blood flow is more restricted. If a blockage cuts off blood flow completely or almost completely, your heart muscle will start to die if not restored. This is a heart attack.<br>
        Don’t ignore any of these symptoms, especially if they are excruciating or last longer than five minutes. Immediate medical treatment is necessary.<br>
        <h3>Symptoms of CAD for women</h3><br>
        Women may also experience the above symptoms, but they’re also more likely to have:
        <li>nausea</li>
        <li>vomiting</li>
        <li>back pain</li>
        <li>jaw pain</li>
        <li>shortness of breath without feeling chest pain
            Men have a higher risk of developing heart disease <br>
            than premenopausal women. Postmenopausal women by age 70 have the same risk as men.<br>
            Due to decreased blood flow, your heart may also:
        <li>become weak</li>
        <li>develop abnormal heart rhythms (arrhythmia) or rates</li>
        <li>fail to pump as much blood as your body needs</li>
        Your doctor will detect these heart abnormalities during diagnosis.<br>


        <h3>Risk factors for CAD</h3><br>
        Understanding the risk factors for CAD can help with your plan to prevent or decrease the likelihood of developing the disease.<br>
        Risk factors include:<br>
        <li>high blood pressure</li>
        <li>high blood cholesterol levels</li>
        <li>tobacco smoking</li>
        <li>insulin resistance/hyperglycemia/diabetes mellitus</li>
        <li>obesity</li>
        <li>inactivity</li>
        <li>unhealthy eating habits</li>
        <li>obstructive sleep apnea</li>
        <li>emotional stress</li>
        <li>excessive alcohol consumption</li>
        <li>history of preeclampsia during pregnancy</li>
        The risk for CAD also increases with age. Based on age alone as a risk factor, men have a greater risk for the disease beginning at age 45 and women have a greater risk beginning at age 55. The risk for coronary artery disease is also higher if you have a family history of the disease.<br>


        <h3>Diagnosing CAD</h3>
        Diagnosing CAD requires a review of your medical history, a physical examination, and other medical testing. These tests include:<br>
        <li><b>Electrocardiogram: </b></li>This test monitors electrical signals that travel through your heart. It may help your doctor determine whether you’ve had a heart attack.<br>
        <li><b>Echocardiogram:</b></li> This imaging test uses ultrasound waves to create a picture of your heart. The results of this test reveal whether certain things in your heart are functioning properly.<br>
        <li><b>Stress test:</b></li> This particular test measures the stress on your heart during physical activity and while at rest. The test monitors your heart’s electrical activity while you walk on a treadmill or ride a stationary bike. Nuclear imaging may also be performed for a portion of this test. For those unable to perform physical exercise, certain medications can be used instead for stress testing.<br>
        <li><b>Cardiac catheterization (left heart catheterization):</b></li> During this procedure, your doctor injects a special dye into your coronary arteries through a catheter inserted through an artery in your groin or forearm. The dye helps enhance the radiographic image of your coronary arteries to identify any blockages.<br>
        <li><b>Heart CT scan:</b></li>Your doctor may use this imaging test to check for calcium deposits in your arteries.

        <h3>What is the treatment for CAD?</h3>
        It’s important to reduce or control your risk factors and seek treatment to lower the chance of a heart attack or stroke, if you’re diagnosed with CAD. Treatment also depends on your current health condition, risk factors, and overall wellbeing. For example, your doctor may prescribe medication therapy to treat high cholesterol or high blood pressure, or you may receive medication to control blood sugar if you have diabetes.<br>
        Lifestyle changes can also reduce your risk of heart disease and stroke. For example:<br>
        <li>quit smoking tobacco</li>
        <li>reduce or stop your consumption of alcohol</li>
        <li>exercise regularly</li>
        <li>lose weight to a healthy level</li>
        <li>eat a healthy diet (low in fat, low in sodium)</li>
        If your condition doesn’t improve with lifestyle changes and medication, your doctor may recommend a procedure to increase blood flow to your heart. These procedures may be:<br>
        <b>balloon angioplasty:</b> to widen blocked arteries and smoosh down the plaque buildup, usually performed with insertion of a stent to help keep the lumen open after the procedure<br>

        <b>coronary artery bypass graft surgery:</b> to restore blood flow to the heart in open chest surgery<br>
        <b>enhanced external counterpulsation:</b> to stimulate the formation of new small blood vessels to naturally bypass clogged arteries in a noninvasive procedure
        <br>
        <h3>What is the outlook for CAD?</h3>
        Everyone’s outlook for CAD is different. You have better chances of preventing extensive damage to your heart the earlier you can start your treatment or implement lifestyle changes.<br>
        It is important to follow your doctor’s instructions. Take medications as directed and make the recommended lifestyle changes. If you have a higher risk for CAD, you can help to prevent the disease by reducing your risk factors.<br>
