        Heart valve surgery. If a faulty heart valve causes your heart failure, your doctor may recommend repairing or replacing it. A surgeon can repair or replace the valves.
   

        Implantable cardioverter-defibrillator (ICD). This device is similar to a pacemaker. It's put under your skin in your chest. Wires lead through your veins and into your heart to keep track of your heart rhythm. If your heart starts to beat at a dangerous rhythm, or if it stops, the ICD tries to pace your heart or shock it back into normal rhythm. An ICD can also act as a pacemaker and speed your heart up if it's going too slow.
