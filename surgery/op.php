            <h2>OPHTHALMOLOGY</h2>
            <p>Ophthalmology is a branch of medicine and surgery (both methods are used) that deals with the anatomy, physiology and diseases of the eyeball and orbit. An ophthalmologist is a specialist in medical and surgical eye disease. Their credentials include a doctorate degree in medicine, followed by an additional four years of Ophthalmology residency training. They may or may not receive residency training in internal medicine, pediatrics, or general surgery before the ophthalmology residency. Additional training may be sought through a fellowship in a particular specialty of eye pathology. Ophthalmologists are allowed to medically treat eye disease, implement laser therapy, and perform surgery when needed.[4] Ophthalmologists may participate in academic research on the diagnosis and treatment for eye disorders.</p>
        
            <h2>DISEASES</h2>
            <hr>
            A partial list of the most common diseases diagnosed and treated by Ophthalmologists include:
            <li>Cataract</li>
            <li>Glaucoma</li>
            <li>Macular degeneration</li>
            <li>Diabetic retinopathy</li>
            <li>Dry eyes</li>
            <li>Strabismus</li>
            <li>Proptosis</li>
            <li>Excessive tearing</li>
            <li>Uveitis</li>
            <li>Eye tumors</li>
            <li>Refractive surgery</li>

    
            <h2>DIAGNOSIS</h2>
            <hr>
            <h1>EYE EXAMINATION</h1>
            Following are examples of methods of diagnosis performed in a eye examination
            <ul>
                <li> Visual acuity checked by optometrist</li>
                <li> Refraction done by optometrist</li>
                <li> Intraocular pressure measured by optometrist and ophthalmologist</li>
                <li>Slit lamp examination done by both optometrist and ophthalmologist</li>
                <li> Retina examination</li>
            </ul>
            <h3>specialized tests</h3>
            <ul>
                <li>Optical coherence tomography performed by Optometrist and ophthalmic technician</li>
                <li>Fluorescein angiography performed by Optometrist and Ophthalmic technician</li>
                <li> Ultrasonography</li>
            </ul>

            <hr>
            <h4>Ophthalmic surgery</h4>
            <p>Eye surgery, also known as ocular surgery, is surgery performed on the eye or its adnexa by an ophthalmologist.The eye is a fragile organ, and requires extreme care before, during, and after asurgical procedure. An expert eye surgeon is responsible for selecting the appropriate surgical procedure for the patient, and for taking the necessary safety precautions. </p>

            <hr>
            <h2>Subspecialties</h2>
            <p>Ophthalmology includes subspecialities which deal either with certain diseases or diseases of certain parts of the eye. Some of them are:</p>
            <ul>
                <li> Anterior segment surgery</li>
                <li> Cornea, ocular surface, and external disease</li>
                <li>Glaucoma</li>
                <li> Medical retina, deals with treatment of retinal problems through non-surgical means.</li>
                <li> Vitreo-retinal surgery, deals with surgical management of retinal and posterior segment diseases and disorders. Medical retina and vitreo-retinal surgery sometimes together called posterior segment subspecialisation, whereas management and research of the vitreousexclusively is sometimes referred to as vitreology</li>
                <li> Neuro-ophthalmology</li>
                <li> Ocular oncology</li>
                <li>Oculoplastics and orbit surgery</li>
                <li>Ophthalmic pathology</li>
                <li> Pediatric ophthalmology/strabismus (misalignment of the eyes)</li>
                <li>Refractive surgery</li>
                <li>Uveitis</li>
                <li> Veterinary specialty training programs in veterinary ophthalmology exist in some countries.</li>
            </ul>



            <h2>Signs and Symptoms of Vision Problems</h2>
            <p>If you experience any of the following eye changes, schedule an appointment with your ophthalmologist or optometrist immediately, even if you've been to your eye doctor recently:</p>
            <ul>
                <li>Severe, sudden eye pain</li>
                <li>Recurrent pain in or around the eye</li>
                <li>Hazy, blurred, or double vision</li>
                <li> Seeing flashes of light or sudden bright floating spots</li>
                <li>Seeing rainbows or halos around lights</li>
                <li>Seeing floating "spider webs"</li>
                <li>Seeing a "curtain coming down" over one eye</li>
                <li>Sensing a "cup filling up with ink" in one eye</li>
                <li>Unusual, even painful, sensitivity to light or glare</li>
                <li> Swollen, red eyes</li>
                <li>Changes in the color of the iris</li>
                <li>White areas in the pupil of the eye</li>
                <li>Sudden development of persistent floaters</li>
                <li>Itching, burning, or a heavy discharge in the eyes</li>
                <li>Any sudden change in vision</li>
                <li>See also Normal Vision Changes to help you understand normal age-related changes in the eyes and your vision.</li>
            </ul>
        
