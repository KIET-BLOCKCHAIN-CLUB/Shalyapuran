                <br>
               
                <p>Otorhinolaryngology  (also called otolaryngology and otolaryngology–head and neck surgery) is a surgical subspecialty within medicine that deals with conditions of the ear, nose, and throat (ENT) and related structures of the head and neck. Doctors who specialize in this area are called otorhinolaryngologists, otolaryngologists, ENT doctors, ENT surgeons, or head and neck surgeons. Patients seek treatment from an otorhinolaryngologist for diseases of the ear, nose, throat, base of the skull, and for the surgical management of cancers and benign tumors of the head and neck. </p>
                
                
                
                  
                
                
